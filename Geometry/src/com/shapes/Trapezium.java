package com.shapes;

public class Trapezium extends Quadrilateral {
	
	public Trapezium(double dim1, double dim2, double dim3, double dim4) {
		super(dim1, dim2, dim3, dim4);
	}

	@Override
	public double perimeter() {
		// Perimeter
		return super.perimeter();
	}

	@Override
	public double area() {
		// Area
		return super.area();
	}



}